// code to find area of circle
public class HelloWorldExample{
 
  public static void main(String args[]){
   
   
   double radius = 4;
   
   // circle area = 3.14 * (radius * radius) 
   
   double area = 3.14 * (radius * radius);
    
   
   System.out.print("area = "+ area);


  }
 
}